const mongoose = require("mongoose");

const SignUpSchema = new mongoose.Schema({
  fullName: {
    type: String,
    required: true,
  },
  userName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const SignUpTemplate = mongoose.model("signup", SignUpSchema);

module.exports = { SignUpTemplate };
